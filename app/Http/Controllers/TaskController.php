     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Task $task)
    {
        if($request->status === '1'){
            $task->done_at = now();
            $task->save();
        }else{
            $task->done_at = null;
            $task->save();
        }

        return redirect()->back();
    }

